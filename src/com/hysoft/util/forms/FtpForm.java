package com.hysoft.util.forms;

public class FtpForm {
    private String ftpPath;
    private String ftpAddress;
    private int ftpPort;
    private String ftpLoginName;
    private String ftpPwd;
    private String desc;
    private String localTempPath;
    private String secondPath;
    private String serverId;
    
	public String getServerId() {
		return serverId;
	}
	public void setServerId(String serverId) {
		this.serverId = serverId;
	}
	public String getLocalTempPath() {
		return localTempPath;
	}
	public void setLocalTempPath(String localTempPath) {
		this.localTempPath = localTempPath;
	}
	public String getSecondPath() {
		return secondPath;
	}
	public void setSecondPath(String secondPath) {
		this.secondPath = secondPath;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getFtpPath() {
		return ftpPath;
	}
	public void setFtpPath(String ftpPath) {
		this.ftpPath = ftpPath;
	}
	public String getFtpAddress() {
		return ftpAddress;
	}
	public void setFtpAddress(String ftpAddress) {
		this.ftpAddress = ftpAddress;
	}
	public int getFtpPort() {
		return ftpPort;
	}
	public void setFtpPort(int ftpPort) {
		this.ftpPort = ftpPort;
	}
	public String getFtpLoginName() {
		return ftpLoginName;
	}
	public void setFtpLoginName(String ftpLoginName) {
		this.ftpLoginName = ftpLoginName;
	}
	public String getFtpPwd() {
		return ftpPwd;
	}
	public void setFtpPwd(String ftpPwd) {
		this.ftpPwd = ftpPwd;
	}
	   
   
}
